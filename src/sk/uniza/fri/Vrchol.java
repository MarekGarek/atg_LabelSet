package sk.uniza.fri;

public class Vrchol {

    private int vrchol;
    private int cena;

    public Vrchol(int vrchol, int cena) {
        this.vrchol = vrchol;
        this.cena = cena;
    }

    public int getVrchol() {
        return this.vrchol;
    }

    public void setVrchol(int vrchol) {
        this.vrchol = vrchol;
    }

    public int getCena() {
        return this.cena;
    }

    public void setCena(int cena) {
        this.cena = cena;
    }

}
